export const leftToRightStackAnimation = {
    cardStyleInterpolator: ({ current, layouts }:{current:any, layouts:any}) => {
      return {
        cardStyle: {
          transform: [
            {
              translateX: current.progress.interpolate({
                inputRange: [0, 1],
                outputRange: [layouts.screen.width, 0],
              }),
            },
          ],
        },
      };
    },
  };